package common

import (
	"fmt"
	"runtime"
)

func StringPtr(str string) *string {
	return &str
}

func IntPtr(i int) *int {
	return &i
}

func UintPtr(i uint) *uint {
	return &i
}

func BoolPtr(b bool) *bool {
	return &b
}

func PrintStack() {
	var buf [4096]byte
	n := runtime.Stack(buf[:], false)
	fmt.Printf("==> %s\n", string(buf[:n]))
}
